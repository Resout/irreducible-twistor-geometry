"""
Rigorous verification: N-1 SU(2) subgroups generate SU(N).

For the paper, we need to prove:
1. N-1 specific SU(2) subgroups of SU(N) generate the full Lie algebra su(N)
2. The cross-conflicts between DS systems correspond to the commutators
3. The DoF counting is correct

The standard result: su(N) is generated by the simple roots.
SU(N) has rank N-1, with N-1 simple roots.
Each simple root defines an SU(2) subalgebra (the "root SU(2)").
These N-1 root SU(2)'s generate all of su(N).
This is the content of the Chevalley-Serre relations.
"""
import numpy as np

# ============================================================
# Verify for SU(2), SU(3), SU(4)
# ============================================================

def gell_mann(N):
    """Generate the generalized Gell-Mann matrices for SU(N)."""
    matrices = []

    # Symmetric off-diagonal
    for j in range(N):
        for k in range(j+1, N):
            m = np.zeros((N, N), dtype=complex)
            m[j, k] = 1
            m[k, j] = 1
            matrices.append(m)

    # Antisymmetric off-diagonal
    for j in range(N):
        for k in range(j+1, N):
            m = np.zeros((N, N), dtype=complex)
            m[j, k] = -1j
            m[k, j] = 1j
            matrices.append(m)

    # Diagonal
    for l in range(1, N):
        m = np.zeros((N, N), dtype=complex)
        for j in range(l):
            m[j, j] = 1
        m[l, l] = -l
        m = m * np.sqrt(2 / (l * (l + 1)))
        matrices.append(m)

    return matrices

def commutator(A, B):
    return A @ B - B @ A

def check_su2_generation(N):
    """
    Check that N-1 root SU(2) subalgebras generate all of su(N).

    The root SU(2)_k (k=1,...,N-1) acts on the (k, k+1) block.
    Its generators are:
      E_k^+ (raising): matrix with 1 at (k-1, k)
      E_k^- (lowering): matrix with 1 at (k, k-1)
      H_k (Cartan): matrix with 1 at (k-1,k-1) and -1 at (k,k)

    Claim: repeated commutators of these generate all of su(N).
    """
    generators = gell_mann(N)
    dim_suN = N**2 - 1

    # Root SU(2) generators: act on (k, k+1) blocks
    root_generators = []
    for k in range(N-1):
        # E_k^+ (off-diagonal symmetric)
        e_plus = np.zeros((N, N), dtype=complex)
        e_plus[k, k+1] = 1
        e_plus[k+1, k] = 1

        # E_k^- (off-diagonal antisymmetric)
        e_minus = np.zeros((N, N), dtype=complex)
        e_minus[k, k+1] = -1j
        e_minus[k+1, k] = 1j

        # H_k (diagonal)
        h_k = np.zeros((N, N), dtype=complex)
        h_k[k, k] = 1
        h_k[k+1, k+1] = -1

        root_generators.extend([e_plus, e_minus, h_k])

    # Now generate the full algebra by taking commutators
    # Start with the root generators
    basis = [g.copy() for g in root_generators]

    # Iteratively add commutators until no new generators appear
    for iteration in range(20):
        new_found = False
        to_add = []
        for i in range(len(basis)):
            for j in range(i+1, len(basis)):
                comm = commutator(basis[i], basis[j])
                # Check if comm is linearly independent of existing basis
                if np.linalg.norm(comm) < 1e-10:
                    continue
                # Normalize
                comm = comm / np.linalg.norm(comm)
                # Check independence
                independent = True
                for b in basis + to_add:
                    b_norm = b / np.linalg.norm(b) if np.linalg.norm(b) > 1e-10 else b
                    overlap = np.abs(np.trace(comm @ b_norm.conj().T))
                    if overlap > N - 0.1:  # nearly parallel
                        independent = False
                        break
                if independent:
                    to_add.append(comm)
                    new_found = True

        basis.extend(to_add)
        if not new_found:
            break

    # Count independent generators by computing rank of the matrix
    # where each generator is a flattened vector
    mat = np.array([g.flatten() for g in basis])
    rank = np.linalg.matrix_rank(mat, tol=1e-6)

    return rank, dim_suN, len(root_generators)


# The Cartan matrix for SU(N) is:
# A_ij = 2δ_ij - δ_{i,j+1} - δ_{i,j-1}
# For SU(3): A = [[2, -1], [-1, 2]]
# For SU(4): A = [[2,-1,0],[-1,2,-1],[0,-1,2]]

for N in range(2, 6):
    A = np.zeros((N-1, N-1))
    for i in range(N-1):
        A[i, i] = 2
        if i > 0: A[i, i-1] = -1
        if i < N-2: A[i, i+1] = -1

    print(f"SU({N}) Cartan matrix ({N-1}×{N-1}):")
    for i in range(N-1):
        row = "  ["
        for j in range(N-1):
            row += f" {A[i,j]:+.0f}"
        row += " ]"
        print(row)

    # Off-diagonal entries: A_ij = -1 when roots i and j are adjacent
    # = 0 when they're not adjacent
    # This is the Dynkin diagram: A_{N-1} is a chain
    n_couplings = np.sum(A != 0) - (N-1)  # off-diagonal nonzero entries
    print(f"  Couplings (off-diag nonzero): {int(n_couplings)}")
    print(f"  Dynkin diagram: A_{N-1} (chain of {N-1} nodes)")
    print()


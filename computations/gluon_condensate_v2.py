# ============================================================
# The path integral: sample configurations, measure F^2
# ============================================================
#
# Each "configuration" is a lattice of mass functions m(x).
# The weight is the DS dynamics: configurations near m* are weighted
# by how quickly DS drives toward m*.
#
# For a Monte Carlo estimate: start from random initial conditions,
# evolve a few steps with e* evidence (to approach m* but retain
# spatial variation), measure F^2.
#
# The NUMBER of DS steps determines the "temperature": fewer steps =
# more fluctuation = larger <F^2>. In the path integral language,
# the transfer matrix T = e^{-H} acts for n steps, projecting onto
# low-energy states. At finite n, there's a thermal mixture.
#
# The correct <F^2> is the n->infinity LIMIT where only the ground
# state survives — but the ground state is uniform m*, which has F=0.
# So <F^2> in the vacuum = 0?
#
# No. The point is more subtle. In the path integral:
# <F^2> = integral over ALL configurations of F^2(config) * weight(config)
#
# Even though the dominant configuration (m* uniform) has F=0,
# the fluctuations around it have F != 0, and their contribution
# to the average is nonzero.
#
# The Schwinger function S_2^c(0) = <F^2> - <F>^2 = <F^2> (since <F>=0).
# This equals sum_k |c_k|^2 from the spectral decomposition.
# The coefficients c_k come from the overlap of F^2 with the eigenvectors.
#
# So <F^2> = sum_k |<psi_k|F^2|vacuum>|^2 * lambda_k^0 = sum_k |c_k|^2.
# This is a FINITE sum because the state space is compact.
#
# The computation: sample m ~ FS measure on B, compute F^2(m, neighbours),
# average. The FS measure samples uniformly from B, which IS the path
# integral measure at t=0 (before any DS evolution).


def sample_from_B(n_samples, m_center, sigma_pert):
    """Sample mass functions from a neighbourhood of m_center in B."""
    samples = []
    for _ in range(n_samples):
        perturb = sigma_pert * np.random.randn(4)
        m = m_center + perturb
        m = np.abs(m)
        m /= np.sum(m)
        m = enforce_floor(m)
        samples.append(m)
    return samples

def compute_F_sq_lattice(lattice, N):
    """Compute <F^2> across the lattice."""
    F_sq_total = 0.0
    n_sites = 0

    for ix,iy,iz in iprod(range(N), repeat=3):
        M = to_matrix(lattice[ix,iy,iz])
        Mi = inv(M)

        a = []
        for d in range(3):
            ip=[ix,iy,iz]; im=[ix,iy,iz]
            ip[d]=(ip[d]+1)%N; im[d]=(im[d]-1)%N
            Mp = to_matrix(lattice[tuple(ip)])
            Mm = to_matrix(lattice[tuple(im)])
            a.append(Mi @ (Mp - Mm) / 2.0)

        for mu in range(3):
            for nu in range(mu+1, 3):
                comm = a[mu] @ a[nu] - a[nu] @ a[mu]

                # Plaquette: use Wilson loop around elementary square
                # U_plaq = M(x)^{-1} M(x+mu) M(x+mu)^{-1}M(x+mu+nu) ...
                # Simpler: use F = [a_mu, a_nu] as leading order
                # (the da term is second order in the perturbation)

                F_mn = comm  # leading order
                F_sq_total += np.real(np.trace(F_mn.conj().T @ F_mn))

        n_sites += 1

    return F_sq_total / n_sites

